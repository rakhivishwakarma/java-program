class Swap
{
  public static void main(String args[])
 {
   int a=3,b=5;
   int temp;
   System.out.println("Before Swap:a="+a);
   System.out.println("After Swap:a="+b);
   temp=a;
   a=b;
   b=temp;
   System.out.println("Before Swap:b="+b);
   System.out.println("After Swap:b="+a);
  }
}
 